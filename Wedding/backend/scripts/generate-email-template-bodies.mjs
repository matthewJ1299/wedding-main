/**
 * One-shot generator: read Canva/TextEdit RTF-wrapped HTML from Wedding/*.html|txt,
 * strip RTF, apply merge-field placeholders, write `src/seed/generatedEmailBodies.js`.
 *
 * Run from repo: `node scripts/generate-email-template-bodies.mjs`
 * (cwd: Wedding/backend)
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { stripRtfToEmailHtml } from '../src/utils/rtfEmailHtmlStripper.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const backendRoot = path.join(__dirname, '..');
const weddingRoot = path.join(backendRoot, '..');

const inputs = [
  { inFile: path.join(weddingRoot, 'invite.html'), key: 'invite' },
  { inFile: path.join(weddingRoot, 'rsvpyes.html'), key: 'rsvpYes' },
  { inFile: path.join(weddingRoot, 'rsvpno.txt'), key: 'rsvpNo' },
];

function applyInvitePlaceholders(html) {
  let h = html;
  h = h.replace(/\[Name\]/g, '{guestName}');
  h = h.replace(/\{websiteLink\}/g, '{rsvpLink}');
  h = h.replace(/https:\/\/matthewandsydney\.co\.za/g, '{rsvpLink}');
  h = h.replace(/#ffffff">Website/g, '#ffffff">RSVP');
  h = h.replace(/Saturday,\s*03\s*October\s*2026/g, '{weddingDate}');
  h = h.replace(
    /<span style="white-space:pre-wrap">Please arrive at<\/span><span style="white-space:pre-wrap"> <\/span><span style="white-space:pre-wrap">15:00<\/span>/g,
    '<span style="white-space:pre-wrap">Please arrive at</span><span style="white-space:pre-wrap"> </span><span style="white-space:pre-wrap">{weddingTime}</span>'
  );
  h = h.replace(/De Harte Venue/g, '{weddingLocation}');
  h = h.replace(/206 Rentia Street, Onderstepoort, Pretoria/g, '{eventAddress}');
  h = h.replace(/RSVP by 03 June 2026/g, 'RSVP by {rsvpDeadline}');
  return h;
}

function applyRsvpYesPlaceholders(html) {
  let h = html;
  h = h.replace(/\[Name\]/g, '{guestName}');
  h = h.replace(/\{websiteLink\}/g, '{rsvpLink}');
  h = h.replace(/https:\/\/matthewandsydney\.co\.za/g, '{rsvpLink}');
  h = h.replace(/#ffffff">Website/g, '#ffffff">RSVP');
  h = h.replace(
    /<span style="font-weight:700;white-space:pre-wrap">Saturday,\s*03\s*October\s*2026<\/span>/g,
    '<span style="font-weight:700;white-space:pre-wrap">{weddingDate}</span>'
  );
  h = h.replace(
    /<span style="font-weight:700;white-space:pre-wrap">15:00 \| De Harte Venue, Onderstepoort, Pretoria<\/span>/g,
    '<span style="font-weight:700;white-space:pre-wrap">{weddingTime} | {eventAddress}</span>'
  );
  return h;
}

function applyRsvpNoPlaceholders(html) {
  let h = html;
  h = h.replace(/\[Name\]/g, '{guestName}');
  h = h.replace(
    /Thank you so much for letting us know We/g,
    'Thank you so much for letting us know. We'
  );
  return h;
}

function htmlToPlainText(html) {
  if (!html) return '';
  return html
    .replace(/<style[\s\S]*?<\/style>/gi, ' ')
    .replace(/<script[\s\S]*?<\/script>/gi, ' ')
    .replace(/<br\s*\/?>/gi, '\n')
    .replace(/<\/p>/gi, '\n')
    .replace(/<[^>]+>/g, ' ')
    .replace(/&nbsp;/g, ' ')
    .replace(/&amp;/g, '&')
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/\s+\n/g, '\n')
    .replace(/\n{3,}/g, '\n\n')
    .replace(/[ \t]{2,}/g, ' ')
    .trim()
    .slice(0, 20000);
}

const bodies = {};
const writeBackNames = { invite: 'invite.html', rsvpYes: 'rsvpyes.html', rsvpNo: 'rsvpno.txt' };

for (const { inFile, key } of inputs) {
  const raw = fs.readFileSync(inFile, 'utf8');
  let html = stripRtfToEmailHtml(raw);
  if (key === 'invite') html = applyInvitePlaceholders(html);
  if (key === 'rsvpYes') html = applyRsvpYesPlaceholders(html);
  if (key === 'rsvpNo') html = applyRsvpNoPlaceholders(html);
  bodies[key] = { html, text: htmlToPlainText(html) };

  const wb = path.join(weddingRoot, writeBackNames[key]);
  fs.writeFileSync(wb, html, 'utf8');
  console.log('Updated', wb);
}

const outPath = path.join(backendRoot, 'src', 'seed', 'generatedEmailBodies.js');
const payload = `/* eslint-disable max-len */
/* Auto-generated by scripts/generate-email-template-bodies.mjs — do not edit by hand */
export const GENERATED_EMAIL_BODIES = ${JSON.stringify(bodies, null, 0)};
`;

fs.writeFileSync(outPath, payload, 'utf8');
console.log('Wrote', outPath);
